package Day2_042422;

public class LinearArray {
    public static void main(String[] args) {
        // create string array for zipcode
        String[] zipcode = new String[]{"11218","10001","11417","11203"};
        System.out.println("My first zipcode is "+zipcode[0]+ " and last zipcode is "+zipcode[3]);

        // create int array for house number
        int[] houseNumber = new int[]{222,333,444,555};
        System.out.println("My house number is "+ houseNumber[0]+ " and my third house number is " + houseNumber[2]);

    }// end of main method

}//end of java class
